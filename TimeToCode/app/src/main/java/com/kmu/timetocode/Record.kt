package com.kmu.timetocode

class Record(num: Int, title: String, resId: Int) {
    var num: Int? = num
    var title: String? = title
    var resId: Int? = resId

    fun setNum() {
        num = num
    }
    fun setTitle() {
        title = title
    }
    fun setResId() {
        resId = resId
    }
}
